<template>
  <ToolLayout title="HTML 过滤工具">
    <div class="html-filter">
      <div class="tool-bar">
        <button class="btn" @click="filterTags">过滤标签</button>
        <button class="btn" @click="filterAll">过滤全部HTML</button>
        <button class="btn btn-outline" @click="copyResult">复制</button>
      </div>
      <div class="editor-wrap">
        <textarea v-model="input" class="code-area" placeholder="<div class='main'><p>这是<strong>段落</strong>内容</p></div>"></textarea>
        <textarea v-model="output" class="code-area" readonly placeholder="过滤后的纯文本..."></textarea>
      </div>
    </div>
  </ToolLayout>
</template>

<script setup>
import { ref } from 'vue'
import ToolLayout from '../../components/ToolLayout.vue'
const input = ref(''), output = ref('')

function filterTags() {
  output.value = input.value.replace(/<[^>]+>/g, '')
}

function filterAll() {
  output.value = input.value
    .replace(/<style[^>]*>[\s\S]*?<\/style>/gi, '')
    .replace(/<script[^>]*>[\s\S]*?<\/script>/gi, '')
    .replace(/<[^>]+>/g, '')
    .replace(/&nbsp;/g, ' ')
    .replace(/&amp;/g, '&').replace(/&lt;/g, '<').replace(/&gt;/g, '>').replace(/&quot;/g, '"')
    .replace(/\s{2,}/g, ' ')
    .trim()
}

function copyResult() { if (!output.value) return; navigator.clipboard.writeText(output.value).then(() => alert('✅ 已复制')) }
</script>

